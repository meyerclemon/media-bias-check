function sourceColors(source) {
  var colors = [];

  for (var color in boxes) {
    var boxArr = boxes[color];

    for (var i = 0; i <= boxArr.length; i++) {
      var verticalRange = boxArr[i].verticalRange;
      var horizontalRange = boxArr[i].verticalRange;
      var isInVerticalRange = false;
      var isInHorizontalRange = false;

      // Is the source in the box horizontally
      if (
        horizontalRange[0] <= source["Horizontal Rank"] &&
        source["Horizontal Rank"] <= horizontalRange[1]
      ) {
        isInHorizontalRange = true;
      }

      if (
        verticalRange[0] <= source["Vertical Rank"] &&
        source["Vertical Rank"] <= verticalRange[1]
      ) {
        isInVerticalRange = true;
      }

      if (horizontalRange && verticalRange) {
        color.push(color);
      }
    }
  }

  return colors;
}

